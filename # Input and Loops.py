# Ask the user a question and store the answer in a variable
# Ask the user for their name and store it
name = input("Hey, What is your name? ")

# Ask the user for two numbers and then add them together
num1 = int(input("\nWhat is your first number? "))
num2 = int(input("What is your second number? "))
print(f"\nYour first number was {num1}")
print(f"Your second number was {num2}")

# Adding the input together
sum = num1 + num2
print(f"The sum of the two numbers is {sum}\n")

# Multiply the input together
product = num1 * num2
print(f"The product of the two numbers is {product}\n")

# Test that the input was stored correctly
print(f"Nice to meet you {name}!")
