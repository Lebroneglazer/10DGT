# Ask the user a question and store their answer in a variable 
# Ask the user for their name and store it
name = input("what is your name?")

# Ask the user for two numbers and add them together.
num1 = int(input("what is your first number?"))
num2 = int(input("what is your second number?"))
print(f"You first number was {num1}")
print(f"You second number was {num2}")

# To check that the input was stored correctly
print(num1)
print(num2)

# Adding the input together
sum = num1 + num2
print(sum)

